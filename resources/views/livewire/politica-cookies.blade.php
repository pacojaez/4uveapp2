    <h2 class="m-4 text-6xl text-center font-titles">POLÍTICAS DE COOKIES</h2>
    <p class="p-4 m-2 text-2xl font-light">Una Cookie es un fichero que se descarga en su ordenador al acceder a determinadas páginas web. Las cookies
        permiten a una página web, entre otras cosas, almacenar y recuperar información sobre los hábitos de navegación
        de un usuario o de su equipo y, dependiendo de la información que contenga y de la forma en que utilice su
        equipo, pueden utilizarse para reconocer al usuario.
        Las cookies son esenciales para el funcionamiento de internet, aportando innumerables ventajas en la prestación
        de servicios interactivos, facilitándole la navegación y usabilidad de nuestra web.
        La información que le proporcionamos a continuación, le ayudará a comprender los diferentes tipos de cookies:
    </p>
    <hr>
    <h3 class="p-4 text-4xl font-light">TIPOS DE COOKIES SEGÚN LA ENTIDAD QUE LAS GESTIONE</h3>
    <h4 class="p-4 text-2xl font-light underline"> Cookies propias:</h4>
    <p class="p-4 m-2 text-2xl font-light"> Son aquellas que se recaban por el propio editor para prestar el servicio solicitado por el usuario.</p>
    <h4 class="p-4 text-2xl font-light underline">Cookies de tercero</h4>
    <p class="p-4 m-2 text-2xl font-light">Son aquellas que son recabadas y gestionadas por un tercero, estás no se pueden considerar propias.</p>
    <hr>
    <h3 class="p-4 text-4xl font-light">SEGÚN EL PLAZO DE TIEMPO QUE PERMANEZCAN ACTIVADAS</h3>
    <h4 class="p-4 text-2xl font-light underline">Cookies de sesión</h4>
    <p class="p-4 m-2 text-2xl font-light">Recaban datos mientras el usuario navega por la red con la finalidad de prestar el servicio solicitado.</p>
    <h4 class="p-4 text-2xl font-light underline">Cookies persistentes</h4>
    <p class="p-4 m-2 text-2xl font-light">Se almacenan en el terminal y la información obtenida, será utilizada por el responsable de la cookie con la
        finalidad de prestar el servicio solicitado.</p>
    <hr>
    <h3 class="p-4 text-4xl font-light">SEGÚN SU FINALIDAD</h3>
    <h4 class="p-4 text-2xl font-light underline">Cookies técnicas</h4>
    <p class="p-4 m-2 text-2xl font-light">Son las necesarias para la correcta navegación por la web.</p>
    <h4 class="p-4 text-2xl font-light underline">Cookies de personalización</h4>
    <p class="p-4 m-2 text-2xl font-light">Permiten al usuario las características (idioma) para la navegación por la website.</p>
    <h4 class="p-4 text-2xl font-light underline">Cookies de análisis</h4>
    <p class="p-4 m-2 text-2xl font-light">Permiten al prestador el análisis vinculado a la navegación realizada por el usuario, con la finalidad de llevar
        un seguimiento de uso de la página web, así como realizar estadísticas de los contenidos más visitados, número
        de visitantes, etc.</p>
    <h4 class="p-4 text-2xl font-light underline">Cookies publicitarias</h4>
    <p class="p-4 m-2 text-2xl font-light">Permiten al editor incluir en la web, espacios publicitarios, según el contenido de la propia web.</p>
